// Math 

// PI -returns pie value
const pie= Math.PI;
console.log(pie);

// round - It round off.
console.log(Math.round(pie));

// floor -It removes float.
console.log(Math.floor(pie));

// Ceil -It increases on float.
console.log(Math.ceil(pie));

// min max -return max and min value.
console.log(Math.max(10,11));

// Math.random -returns random value less than 1
console.log(Math.random());

// abs (Absolute) - retuns positive
console.log(Math.abs(-11.5));

// Math.Sqrt -returns square root
console.log(Math.sqrt(4));

// pow - add power to given value (num,power)
console.log(Math.pow(2,4));